class Assento{
    numeroAssento: number;
    ocupado: boolean;
    constructor(numeroAssento: number,ocupado: boolean) {
        this.numeroAssento = numeroAssento;
        this.ocupado = ocupado;
    }
    setAssento(numeroAssento: number,ocupado: boolean) {
        this.numeroAssento = numeroAssento;
        this.ocupado = ocupado;
    }
    
}